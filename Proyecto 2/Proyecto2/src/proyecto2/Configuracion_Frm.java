     private void Cargar_Archivo(){
        try{
            palabras = _FileHandler.readLines(FileName);
           }
        catch(IOException e)
            {
                e.printStackTrace();
            }
        
    }
    
     private void Limpiar_Array(){
        //El siguiente es un HashSet que va ayudar a eliminar duplicados
        //Luego se convierte de HashSet a array y asi queda limpio.
        Set<String> palabras_Set = new HashSet<String>(Arrays.asList(palabras));
        palabras = palabras_Set.toArray(new String[0]);
        
    }
     
    private void Mostrar_Archivo(){
        //Este metodo borra el Textarea y lo vuelve a rellenar
        jTextArea1.setText("");  // Se deja el TextArea en limpio.
        //Se recorre el array para hacer un append al textarea:
        for(int i = 0; i< palabras.length; i++ ) {
            jTextArea1.append(palabras[i].toString().toLowerCase());
            if (i < palabras.length-1) {
                jTextArea1.append("\n");
            }
        }
    }
     
    private void Show_Counter(){
        //Para llevar un contador visual.
        int total_palabras = palabras.length;
        String str1 = Integer.toString(total_palabras);
        Total_Label.setText(str1);
    }
    
    
    private void TF2Array(){
        palabras = jTextArea1.getText().split("\n");
    }
    
   

